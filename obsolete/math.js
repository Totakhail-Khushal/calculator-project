

// basic math operations

export function add(a, b) {
    return a + b;
}

function sub(a,b) {
    return a - b;
}

function multiply(a,b) {
    return a * b;
}

function divide(a,b) {
    return a / b;
}

function percentage(a,b) {
    return a / 100 * b;
}

function squareRoot(a) {
    return a**(1/2);
}

function cubicRoot(a) {
    return a**(1/3);
}